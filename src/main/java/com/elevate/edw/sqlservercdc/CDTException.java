package com.elevate.edw.sqlservercdc;

public class CDTException extends Exception {

	public CDTException() {
		super();
	}

	public CDTException(String message) {
		super(message);
		
	}

	public CDTException(Throwable cause) {
		super(cause);
		
	}

	public CDTException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public CDTException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
